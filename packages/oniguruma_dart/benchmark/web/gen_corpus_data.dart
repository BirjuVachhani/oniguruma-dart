/// Generates `corpus_data.g.dart` — the two benchmark corpora embedded as
/// base64 string constants, so the web/JS harness (which has no `dart:io`) can
/// run over the EXACT same bytes as the VM and C harnesses.
///
///   dart run benchmark/web/gen_corpus_data.dart
library;

import 'dart:convert';
import 'dart:io';

void main() {
  final ascii = base64.encode(
      File('benchmark/datasets/corpus.txt').readAsBytesSync());
  final uni = base64.encode(
      File('benchmark/datasets/unicode_corpus.txt').readAsBytesSync());

  final buf = StringBuffer()
    ..writeln('// GENERATED by benchmark/web/gen_corpus_data.dart — do NOT edit.')
    ..writeln('// The two benchmark corpora, base64-encoded, so the dart2js/web')
    ..writeln('// harness can decode identical bytes without dart:io file access.')
    ..writeln('library;')
    ..writeln()
    ..writeln("const String asciiB64 =")
    ..writeln("    '$ascii';")
    ..writeln()
    ..writeln("const String uniB64 =")
    ..writeln("    '$uni';");

  File('benchmark/web/corpus_data.g.dart').writeAsStringSync(buf.toString());
  stdout.writeln('wrote benchmark/web/corpus_data.g.dart '
      '(ascii ${ascii.length}B b64, uni ${uni.length}B b64)');
}
