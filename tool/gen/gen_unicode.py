#!/usr/bin/env python3
"""Generate Dart Unicode data files from Oniguruma's C tables.

Reads oniguruma-master/src/unicode_property_data.c and unicode_fold_data.c and
emits:
  lib/src/unicode/data/property_ranges.g.dart  — CR_<Name> code-point ranges
  lib/src/unicode/data/fold.g.dart             — case-fold equivalence tables

Run from the project root:  python3 tool/gen/gen_unicode.py
"""
import re
import sys
import os

ROOT = os.path.dirname(os.path.dirname(os.path.dirname(os.path.abspath(__file__))))
SRC = os.path.join(ROOT, 'oniguruma-master', 'src')
OUT = os.path.join(ROOT, 'lib', 'src', 'unicode', 'data')

NUM = re.compile(r'0x[0-9A-Fa-f]+|\b\d+\b')


def strip_comments(text):
    text = re.sub(r'/\*.*?\*/', ' ', text, flags=re.S)
    text = re.sub(r'//[^\n]*', ' ', text)
    # Drop preprocessor lines (e.g. `#define FOLDS1_END_INDEX 4299`) that appear
    # interspersed in the data arrays — their numbers must not be parsed.
    text = re.sub(r'(?m)^\s*#[^\n]*', ' ', text)
    return text


def parse_property_ranges():
    """Return {name: [lo,hi,lo,hi,...]} from unicode_property_data.c."""
    path = os.path.join(SRC, 'unicode_property_data.c')
    text = open(path, encoding='utf-8', errors='replace').read()
    # Each table: CR_<Name>[] = { <count>, <lo>,<hi>, ... };
    result = {}
    for m in re.finditer(r'CR_([A-Za-z0-9_]+)\s*\[\s*\]\s*=\s*\{(.*?)\}\s*;', text, flags=re.S):
        name = m.group(1)
        body = strip_comments(m.group(2))
        nums = [int(x, 0) for x in NUM.findall(body)]
        if not nums:
            result[name] = []
            continue
        count = nums[0]
        pairs = nums[1:]
        # count is number of ranges; expect 2*count values
        pairs = pairs[:count * 2]
        result[name] = pairs
    # Resolve `#define CR_<Alias> CR_<Target>` aliases (e.g. Nd → Digit).
    for a in re.finditer(r'#define\s+CR_([A-Za-z0-9_]+)\s+CR_([A-Za-z0-9_]+)', text):
        alias, target = a.group(1), a.group(2)
        if target in result and alias not in result:
            result[alias] = result[target]
    return result


def parse_folds():
    """Return (fold1, fold2, fold3).
    fold1: {from:[to,...]} single-code-point keys.
    fold2: list of ([c0,c1],[to...]).
    fold3: list of ([c0,c1,c2],[to...]).
    """
    path = os.path.join(SRC, 'unicode_fold_data.c')
    text = open(path, encoding='utf-8', errors='replace').read()

    def grab(sym):
        m = re.search(re.escape(sym) + r'\s*\[\s*\]\s*=\s*\{(.*?)\}\s*;', text, flags=re.S)
        if not m:
            return []
        body = strip_comments(m.group(1))
        return [int(x, 0) for x in NUM.findall(body)]

    n1 = grab('OnigUnicodeFolds1')
    n2 = grab('OnigUnicodeFolds2')
    n3 = grab('OnigUnicodeFolds3')

    fold1 = {}
    i = 0
    while i + 2 <= len(n1):
        frm = n1[i]
        cnt = n1[i + 1]
        if i + 2 + cnt > len(n1):
            break
        fold1[frm] = n1[i + 2:i + 2 + cnt]
        i += 2 + cnt

    fold2 = []
    i = 0
    while i + 3 <= len(n2):
        c0, c1 = n2[i], n2[i + 1]
        cnt = n2[i + 2]
        if i + 3 + cnt > len(n2):
            break
        fold2.append(([c0, c1], n2[i + 3:i + 3 + cnt]))
        i += 3 + cnt

    fold3 = []
    i = 0
    while i + 4 <= len(n3):
        c0, c1, c2 = n3[i], n3[i + 1], n3[i + 2]
        cnt = n3[i + 3]
        if i + 4 + cnt > len(n3):
            break
        fold3.append(([c0, c1, c2], n3[i + 4:i + 4 + cnt]))
        i += 4 + cnt

    return fold1, fold2, fold3


def emit_property_ranges(props):
    os.makedirs(OUT, exist_ok=True)
    path = os.path.join(OUT, 'property_ranges.g.dart')
    with open(path, 'w') as f:
        f.write('// GENERATED by tool/gen/gen_unicode.py — do not edit.\n')
        f.write('// Code-point ranges for Unicode properties (CR_<Name> tables).\n')
        f.write('// Each value is a flat list of [lo, hi] pairs (inclusive).\n\n')
        f.write('const Map<String, List<int>> unicodeCrRanges = {\n')
        for name in sorted(props):
            vals = props[name]
            f.write("  '%s': [%s],\n" % (name, ', '.join(str(v) for v in vals)))
        f.write('};\n')
    return path, len(props)


def emit_folds(fold1, fold2, fold3):
    path = os.path.join(OUT, 'fold.g.dart')
    with open(path, 'w') as f:
        f.write('// GENERATED by tool/gen/gen_unicode.py — do not edit.\n')
        f.write('// Unicode case-fold tables (from Oniguruma OnigUnicodeFolds1/2/3).\n\n')
        f.write('/// Single code point -> its case-fold target code point(s).\n')
        f.write('const Map<int, List<int>> unicodeFold1 = {\n')
        for frm in sorted(fold1):
            tos = fold1[frm]
            f.write('  %d: [%s],\n' % (frm, ', '.join(str(v) for v in tos)))
        f.write('};\n\n')

        f.write('/// Two code points [c0,c1] -> fold target(s). Flat: c0,c1,n,to...\n')
        f.write('const List<int> unicodeFold2 = [\n')
        for key, tos in fold2:
            f.write('  %d, %d, %d, %s,\n' % (key[0], key[1], len(tos),
                                              ', '.join(str(v) for v in tos)))
        f.write('];\n\n')

        f.write('/// Three code points [c0,c1,c2] -> fold target(s). Flat: c0,c1,c2,n,to...\n')
        f.write('const List<int> unicodeFold3 = [\n')
        for key, tos in fold3:
            f.write('  %d, %d, %d, %d, %s,\n' % (key[0], key[1], key[2], len(tos),
                                                  ', '.join(str(v) for v in tos)))
        f.write('];\n')
    return path, len(fold1), len(fold2), len(fold3)


def parse_latin1_ctype():
    """Return the 256-entry EncUNICODE_ISO_8859_1_CtypeTable from unicode.c."""
    path = os.path.join(SRC, 'unicode.c')
    text = open(path, encoding='utf-8', errors='replace').read()
    m = re.search(r'EncUNICODE_ISO_8859_1_CtypeTable\s*\[\s*256\s*\]\s*=\s*\{(.*?)\}\s*;',
                  text, flags=re.S)
    if not m:
        return []
    body = strip_comments(m.group(1))
    return [int(x, 0) for x in NUM.findall(body)][:256]


def emit_latin1(table):
    path = os.path.join(OUT, 'latin1_ctype.g.dart')
    with open(path, 'w') as f:
        f.write('// GENERATED by tool/gen/gen_unicode.py — do not edit.\n')
        f.write('// EncUNICODE_ISO_8859_1_CtypeTable: per-code-point ctype bits for 0..255.\n\n')
        f.write('const List<int> unicodeLatin1CtypeTable = [\n')
        for i in range(0, len(table), 16):
            f.write('  ' + ', '.join(str(v) for v in table[i:i + 16]) + ',\n')
        f.write('];\n')
    return path, len(table)


EGCB_CLASSES = ['Other', 'CR', 'LF', 'Control', 'Extend', 'ZWJ', 'Prepend',
                'SpacingMark', 'L', 'V', 'T', 'LV', 'LVT', 'Regional_Indicator']


# UAX#29 Word_Break classes, in the C `enum WB_TYPE` order.
WB_CLASSES = ['Any', 'ALetter', 'CR', 'Double_Quote', 'Extend', 'ExtendNumLet',
              'Format', 'Hebrew_Letter', 'Katakana', 'LF', 'MidLetter',
              'MidNum', 'MidNumLet', 'Newline', 'Numeric', 'Regional_Indicator',
              'Single_Quote', 'WSegSpace', 'ZWJ']


def parse_wb():
    """Return flat [lo, hi, classId, ...] from unicode_wb_data.c."""
    path = os.path.join(SRC, 'unicode_wb_data.c')
    text = open(path, encoding='utf-8', errors='replace').read()
    m = re.search(r'WB_RANGES\s*\[\s*\]\s*=\s*\{(.*?)\}\s*;', text, flags=re.S)
    if not m:
        return []
    out = []
    for rm in re.finditer(r'\{\s*(0x[0-9A-Fa-f]+)\s*,\s*(0x[0-9A-Fa-f]+)\s*,\s*WB_([A-Za-z_]+)\s*\}', m.group(1)):
        lo = int(rm.group(1), 0)
        hi = int(rm.group(2), 0)
        cls = rm.group(3)
        cid = WB_CLASSES.index(cls) if cls in WB_CLASSES else 0
        out.extend([lo, hi, cid])
    return out


def emit_wb(ranges):
    path = os.path.join(OUT, 'wb.g.dart')
    with open(path, 'w') as f:
        f.write('// GENERATED by tool/gen/gen_unicode.py — do not edit.\n')
        f.write('// Word_Break ranges: flat [lo, hi, classId] triples.\n')
        f.write('// classId indexes: %s\n\n' % ', '.join(
            '%d=%s' % (i, n) for i, n in enumerate(WB_CLASSES)))
        f.write('const List<int> wbRanges = [\n')
        for i in range(0, len(ranges), 15):
            f.write('  ' + ', '.join(str(v) for v in ranges[i:i + 15]) + ',\n')
        f.write('];\n')
    return path, len(ranges) // 3


def parse_egcb():
    """Return flat [lo, hi, classId, ...] from unicode_egcb_data.c."""
    path = os.path.join(SRC, 'unicode_egcb_data.c')
    text = open(path, encoding='utf-8', errors='replace').read()
    m = re.search(r'EGCB_RANGES\s*\[\s*\]\s*=\s*\{(.*?)\}\s*;', text, flags=re.S)
    if not m:
        return []
    out = []
    for rm in re.finditer(r'\{\s*(0x[0-9A-Fa-f]+)\s*,\s*(0x[0-9A-Fa-f]+)\s*,\s*EGCB_([A-Za-z_]+)\s*\}', m.group(1)):
        lo = int(rm.group(1), 0)
        hi = int(rm.group(2), 0)
        cls = rm.group(3)
        cid = EGCB_CLASSES.index(cls) if cls in EGCB_CLASSES else 0
        out.extend([lo, hi, cid])
    return out


def emit_egcb(ranges):
    path = os.path.join(OUT, 'egcb.g.dart')
    with open(path, 'w') as f:
        f.write('// GENERATED by tool/gen/gen_unicode.py — do not edit.\n')
        f.write('// Grapheme_Cluster_Break ranges: flat [lo, hi, classId] triples.\n')
        f.write('// classId indexes: %s\n\n' % ', '.join(
            '%d=%s' % (i, n) for i, n in enumerate(EGCB_CLASSES)))
        f.write('const List<int> egcbRanges = [\n')
        for i in range(0, len(ranges), 15):
            f.write('  ' + ', '.join(str(v) for v in ranges[i:i + 15]) + ',\n')
        f.write('];\n')
    return path, len(ranges) // 3


def main():
    props = parse_property_ranges()
    fold1, fold2, fold3 = parse_folds()
    latin1 = parse_latin1_ctype()
    p1, np = emit_property_ranges(props)
    p2, n1, n2, n3 = emit_folds(fold1, fold2, fold3)
    p3, nl = emit_latin1(latin1)
    egcb = parse_egcb()
    p4, ne = emit_egcb(egcb)
    wb = parse_wb()
    pwb, nwb = emit_wb(wb)
    print('wrote %s (%d properties)' % (p1, np))
    print('wrote %s (fold1=%d fold2=%d fold3=%d)' % (p2, n1, n2, n3))
    print('wrote %s (%d latin1 entries)' % (p3, nl))
    print('wrote %s (%d egcb ranges)' % (p4, ne))


if __name__ == '__main__':
    main()
